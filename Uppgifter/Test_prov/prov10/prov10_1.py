def maximum(x, y):
    if x > y:
        return x
    else:
        return y 

print(maximum(3, 7))

print(maximum(11, 3))


def addera(x, y):
    return x + y

print(addera(3, 5))

print(addera(10, 2))


def hej(x):
    return "Hej " + x

print(hej("Ada"))

print(hej("Babbage"))







